X=[0.1,0.3, 0.4, 0.7, 1]
print(' -------------- -------------')
print(' : Значение x : : Сумма s :')
print(' -------------- -------------')
N = 10
for x in X:
    s = 0
    for k in range(1,N+1):
        s += ((-1)**k)*((x**k)/((2*k -1)*(2*k)))
    print(f"{x} \t:\t {s}") #форматированная строка
    #print(str(x) + '\t:\t' + str(s)) #другой вормат записи